import re
stroka = input()
stroka2 = re.sub("[.,;:-?-!()]",'',stroka.lower()) #—É–±–∏—Ä–∞–µ–º  .,;:-?-!()
stroka2=stroka2.split()
dict1={}                                               #—Å–ª–æ–≤–∞—Ä—å-—Å—á–µ—Ç—á–∏–∫
dict2={}                                               #—Å–ª–æ–≤–∞—Ä—å –¥–ª—è —Å–ª–æ–≤ —Å –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ –ø–æ–≤—Ç–æ—Ä—è—é—â–∏–º–∏—Å—è –∑–Ω–∞—á–µ–Ω–∏—è–º–∏
#print('stroka2=',stroka2)
for i in stroka2:                                     #–ø—Ä–æ—Ö–æ–¥–∏–º –ø–æ —Å—Ç—Ä–æ–∫–µ –∏ –∑–∞–ø–∏—Å—ã–≤–∞–µ–º –≤ —Å–ª–æ–≤–∞—Ä—å —Å —É–≤–µ–ª–∏—á–µ–Ω–∏–µ–º value
    dict1[i]=dict1.get(i,0)+1
#print('dict1',dict1)
minimum=min(dict1.values())
for key,value in dict1.items(): # —Ü–∏–∫–ª –¥–ª—è –ø–µ—Ä–µ–±–æ—Ä–∞ –ø–µ—Ä–≤–æ–≥–æ —Å–ª–æ–≤–∞—Ä—è –∏ –Ω–∞–ø–æ–ª–Ω–µ–Ω–∏—è –≤—Ç–æ—Ä–æ–≥–æ —Å–ª–æ–≤–∞—Ä—è –ø–∞—Ä–∞–º–∏ —Å –º–∏–Ω–∏–º–∞–ª—å–Ω—ã–º–∏ –∑–Ω–∞—á–µ–Ω–∏—è–º–∏
    if value==minimum:
        dict2[key]=dict2.get(key,value)
result=min(dict2)
print(result)

