# –§—É–Ω–∫—Ü–∏—è —Ä–µ–∞–ª–∏–∑–æ–≤–∞–Ω–∞ –≤ –º–æ–¥—É–ª–µ math.

from fractions import Fraction as F
from math import*
result=[]
n = int(input())
for i in range(1,n):
    for j in range(n,1,-1):
        if i + j == n and gcd(i,j) == 1 and i<j:
            result.append(F(i,j))
print(max(result))