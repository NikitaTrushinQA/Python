# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 1. –ï—Å–ª–∏ –º–∏–Ω–∏–º–∞–ª—å–Ω—ã—Ö / –º–∞–∫—Å–∏–º–∞–ª—å–Ω—ã—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –Ω–µ—Å–∫–æ–ª—å–∫–æ, —Å–ª–µ–¥—É–µ—Ç –≤–µ—Ä–Ω—É—Ç—å –∏–Ω–¥–µ–∫—Å—ã –ø–µ—Ä–≤–æ–≥–æ –ø–æ –ø–æ—Ä—è–¥–∫—É —ç–ª–µ–º–µ–Ω—Ç–∞.

def get_min_max(data):
    return None if not data else (min(enumerate(data, 0), key=lambda x: x[1])[0], max(enumerate(data, 0), key=lambda x: x[1])[0])

